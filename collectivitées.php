    <h2>Pourquoi soutenir NIRD ?</h2>

    <div class="nird-section">
        <ul>
            <li><strong>Engagement environnemental :</strong> prolonger la durée de vie des équipements numériques et réduire les déchets électroniques.</li>
            <li><strong>Réduction des fractures numériques :</strong> offrir un accès équitable aux outils numériques libres et collaboratifs à tous les élèves, y compris les plus fragiles.</li>
            <li><strong>Levier éducatif et citoyen :</strong> permettre aux élèves de devenir acteurs d’une transformation numérique responsable, en participant au reconditionnement et à l’installation.</li>
            <li><strong>Innovation locale :</strong> expérimenter des solutions numériques adaptées aux besoins du territoire, réplicables ailleurs.</li>
            <li><strong>Sécurité et souveraineté numérique :</strong> Linux et les logiciels libres réduisent la dépendance aux acteurs privés étrangers et renforcent la protection des données.</li>
            <li><strong>Cohérence avec les politiques publiques :</strong> s’inscrire dans les objectifs nationaux et territoriaux de transition écologique et de communs numériques.</li>
            <li><strong>Investissement maîtrisé :</strong> réduire les coûts liés aux licences et à l’achat de matériel neuf en réorientant une partie du parc vers du reconditionné.</li>
        </ul>
    </div>

    <br>

    <h2>Comment accompagner NIRD ?</h2>

    <div class="nird-section">
        <ul>
            <li><strong>Faciliter l’intégration technique :</strong> prévoir la compatibilité et l’accès aux réseaux et serveurs pour les postes Linux.</li>
            <li><strong>Accompagnement financier :</strong> soutenir le transport, le stockage, la logistique et l’achat de pièces détachées si nécessaire.</li>
            <li><strong>Valoriser les initiatives locales :</strong> promouvoir les projets des établissements pilotes dans une stratégie numérique territoriale.</li>
            <li><strong>Mettre en réseau les acteurs locaux :</strong> favoriser les partenariats entre établissements, associations, entreprises locales et structures de l’économie sociale et solidaire.</li>
            <li><strong>Former et sensibiliser :</strong> développer les compétences des personnels techniques et administratifs sur Linux et le reconditionnement.</li>
            <li><strong>Agir sur la gouvernance numérique :</strong> inscrire l’usage de Linux et logiciels libres dans les schémas directeurs, adapter les marchés publics et développer les partenariats locaux.</li>
            <li><strong>Montrer l’exemple :</strong> adopter les logiciels libres dans les services de la collectivité et promouvoir des pratiques numériques responsables.</li>
        </ul>
    </div>

